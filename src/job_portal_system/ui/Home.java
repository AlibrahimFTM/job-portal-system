    private void onEditJob() {
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            return;
        }
        int id = (int) jTable1.getValueAt(row, 0);
        try {
            Job j = jobService.listAllJobs()
                    .stream()
                    .filter(x -> x.getId() == id)
                    .findFirst().orElseThrow();
            j.setTitle(titleField.getText());
            j.setLocation(locationField.getText());
            j.setDescription(descriptionArea.getText());
            jobService.updateJob(j);
               if (user.getType() == UserType.Company || user.getType() == UserType.Profossor) {
        refreshJobsTable();
    } else { 
        loadJob();
    
            
                 }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", ERROR_MESSAGE);
        }
    }

