    private void withdrawSelectedApplication() {
        int row = jTableApp.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Please select an application first.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int jobID = (int) jTableApp.getValueAt(row, 0);
        int studentID = (int) jTableApp.getValueAt(row, 1);


        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to withdraw this application?",
                "Confirm Withdraw", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            new ApplicationService().withdrawApplication(jobID,studentID);
            JOptionPane.showMessageDialog(this,
                    "Application withdrawn successfully.",
                    "Done", JOptionPane.INFORMATION_MESSAGE);
             if(user.getType() == UserType.Student ) {
              loadAppTable(); 
        }
        
        
        else if (user.getType() == UserType.Admin) {
        loadAppAllTable();                  
    }else{
                 loadAppmyTable(); 
     }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    "Error withdrawing application:\n" + ex.getMessage(),
                    "DB Error", JOptionPane.ERROR_MESSAGE);
        }
    }

