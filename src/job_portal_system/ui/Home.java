    private void onJobRowSelected() {
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            return;  // no selection
        }
        // Column indices: 0=ID, 1=Title, 2=Location, 3=Description, 4=PostedAt
        String title = jTable1.getValueAt(row, 1).toString();
        String location = jTable1.getValueAt(row, 2).toString();
        String description = jTable1.getValueAt(row, 3).toString();
//    String postedAt    = jTable1.getValueAt(row, 4).toString();

        titleField.setText(title);
        locationField.setText(location);
        descriptionArea.setText(description);
        // postedAtField.setText(postedAt);
    }

    // load into JTable
    private void loadJob() {
        try {
            List<Job> jobs = jobService.listAllJobs();

            // in initComponents() (or your buildMailPanel()), *after* you call initComponents():
            mailTableModel = new DefaultTableModel(
                    new String[]{"id", "Title", "location", "PostedAt"},
                    0 // start with zero rows
            );
            jTable1.setModel(mailTableModel);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            model.setRowCount(0);  // drops all existing rows

            model.setRowCount(0);
            for (Job j : jobs) {
                model.addRow(new Object[]{
                    j.getId(), j.getTitle(), j.getLocation(),
                    j.getPostedAt().format(DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm"))
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "DB Error", ERROR_MESSAGE);
        }
    }

