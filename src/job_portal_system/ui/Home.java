    private void loadUserTable() {
        try {
            List<User> users = userService.listUsers();

            // in initComponents() (or your buildMailPanel()), *after* you call initComponents():
            mailTableModel = new DefaultTableModel(
                    new String[]{"id", "name", "Email", "Role"}, // column names
                    0 // start with zero rows
            );
            jTableUser.setModel(mailTableModel);

            DefaultTableModel model = (DefaultTableModel) jTableUser.getModel();

            model.setRowCount(0);
            for (User u : users) {
                model.addRow(new Object[]{
                    u.getId(),
                    u.getName(),
                    u.getEmail(),u.getType().name()
                 
                });
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(), "DB Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void onEditUser() {
        int row = jTableUser.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select a user first",
                    "No Selection",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        int userId = (Integer) jTableUser.getValueAt(row, 0);
        String newName = nameField.getText().trim();
        String newEmail = emailField.getText().trim();
        String roleStr = (String) roleCombo.getSelectedItem();
        UserType newRole = UserType.valueOf(roleStr);

        try {
            User u = userService.findById(userId);      // loads full subclass
            u.setName(newName);
            u.setEmail(newEmail);
            u.setType(newRole);

            userService.updateUser(u);

            loadUserTable();  // <-- refresh immediately

            JOptionPane.showMessageDialog(this,
                    "User updated successfully!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(),
                    "Update Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

